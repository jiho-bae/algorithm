const solution = require('./sol_211215.js');

describe('수식 최대화', () => {
  test('50*6-3*2 => 300', () => {
    expect(solution('50*6-3*2')).toBe(300);
  });

  test('100-200*300-500+20 => 60420', () => {
    expect(solution('100-200*300-500+20')).toBe(60420);
  });

  test('2+300-35*20 => 5340', () => {
    expect(solution('2+300-35*20')).toBe(5340);
  });

  test('320-350*2+17-300*75 => 632250 [-, +, *] ', () => {
    expect(solution('320-350*2+17-300*75')).toBe(632250);
  });
});
